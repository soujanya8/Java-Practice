public class Whileloop {
    public static void main(String[] args){

        //while loop loops the block of code as long as specified condition is true.
        int i =0;
        int j=0;
        while(i<5){
            while(j<i)
            {
                System.out.print(" ");
                j++;
                
            }
        j=0;
        System.out.println("*");
        i++;
        //printed slant line 
        }
    }
    
}
